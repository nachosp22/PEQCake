    <section>
        <h2>Datos recabados, finalidad y licitud</h2>
        <p>
            PEQ Cakes podrá tratar datos identificativos y de contacto, datos vinculados a solicitudes realizadas a través
            del sitio web y, en su caso, datos necesarios para la gestión de pedidos y atención al cliente.
        </p>
        <p>
            Las finalidades principales del tratamiento son atender consultas, gestionar la relación comercial,
            tramitar solicitudes y pedidos, y cumplir obligaciones legales.
        </p>
        <p>
            La base jurídica del tratamiento será, según cada caso, el consentimiento de la persona interesada,
            la ejecución de medidas precontractuales o contractuales, y/o el cumplimiento de obligaciones legales.
        </p>
    </section>

    <section>
        <h2>Datos para solicitudes de información</h2>
        <p>
            Cuando una persona usuaria realiza una consulta, PEQ Cakes puede solicitar los datos estrictamente necesarios
            para poder responder, como nombre, correo electrónico, teléfono y contenido del mensaje.
        </p>
        <p>
            Estos datos se tratarán exclusivamente para gestionar y dar respuesta a la solicitud recibida.
        </p>
    </section>

    <section>
        <h2>Operaciones de tratamiento</h2>
        <p>
            El tratamiento de datos puede incluir operaciones como recogida, registro, organización, conservación,
            consulta, utilización, comunicación cuando exista base legal para ello y supresión o bloqueo en los plazos
            legalmente establecidos.
        </p>
        <p>
            PEQ Cakes aplica medidas técnicas y organizativas apropiadas para garantizar la confidencialidad,
            integridad y disponibilidad de la información personal.
        </p>
    </section>

    <section>
        <h2>Conservación de datos</h2>
        <p>
            Los datos personales se conservarán durante el tiempo necesario para cumplir la finalidad para la que fueron
            recabados y, posteriormente, durante los plazos exigidos por la normativa aplicable para atender posibles
            responsabilidades legales.
        </p>
    </section>

    <section>
        <h2>Protección de menores</h2>
        <p>
            PEQ Cakes no solicita intencionadamente datos personales de menores de edad sin autorización de sus
            representantes legales. Si se detecta que se han facilitado datos de menores sin la debida autorización,
            se adoptarán las medidas oportunas para su eliminación.
        </p>
    </section>

    <section>
        <h2>Destinatarios</h2>
        <p>
            Con carácter general, PEQ Cakes no cederá datos personales a terceros, salvo obligación legal o cuando sea
            necesario para la prestación de servicios vinculados a la actividad del sitio web, en calidad de encargados
            del tratamiento y bajo las garantías exigidas por la normativa vigente.
        </p>
    </section>

    <section>
